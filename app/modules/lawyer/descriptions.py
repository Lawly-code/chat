get_lawyer_requests_description = """
Получение списка заявок юриста с фильтрацией по статусу.

Возвращает список заявок, назначенных текущему юристу, с указанным статусом.
- Если статус 'pending', возвращаются все ожидающие заявки.
- Если статус 'processing', возвращаются все заявки в процессе.
- Если статус 'completed', ничего не возвращается.

Требуется авторизация с помощью JWT токена. Пользователь должен быть юристом.
"""

create_lawyer_request_description = """
Создание заявки к юристу от пользователя.

Позволяет пользователю создать заявку на консультацию юриста с описанием проблемы и опциональным документом.

Обязательные параметры:
- description: Описание проблемы или запроса

Необязательные параметры:
- document_bytes: Документ в виде байтов (Word документ)

Требуется авторизация с помощью JWT токена.
"""

update_lawyer_request_description = """
Обновление статуса заявки юристом.

Обязательные параметры:
- request_id: ID заявки для обновления
- status: Новый статус ('pending', 'processing', 'completed')

Необязательные параметры (обязательны только если статус 'completed'):
- document_bytes: Документ в виде байтов (Word документ)
- description: Описание выполненной работы

Требуется авторизация с помощью JWT токена. Пользователь должен быть юристом.
"""

